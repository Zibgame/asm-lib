#include "asm_lib.h"

t_pid	getpid(void)
{
	t_pid	pid;

	__asm__ (
		"mov $39, %%rax\n\t"
		"syscall\n\t"
		"mov %%rax, %0"
		: "=r"(pid)
		:
		: "rax", "rcx", "r11", "memory"
		);
	return (pid);
}

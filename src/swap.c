#include "asm_lib.h"

void	swap(int *a, int *b)
{
	__asm__ (
		"movl (%0), %%eax\n\t"
		"xchgl %%eax, (%1)\n\t"
		"movl %%eax, (%0)\n\t"
		:
		: "r"(a), "r"(b)
		: "eax", "memory"
		);
}

#include "asm_lib.h"

int	add(int a, int b)
{
	int	res;

	__asm__ (
		"mov %1, %%eax\n\t"
		"add %2, %%eax\n\t"
		"mov %%eax, %0"
		: "=r"(res)
		: "r"(a), "r"(b)
		: "eax"
		);
	return (res);
}
